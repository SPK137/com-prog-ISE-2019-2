import java.util.Scanner;

public class Loops{
		public static void main(String[] args){
			var sc = new Scanner(System.in);
			int sum = 0;
			while( true ){
				int data = sc.nextInt();
				if(data < 0) break;
				sum += data;
			}
			System.out.println(sum);
		}	
}
